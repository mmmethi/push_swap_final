#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	char *str;

	str = s1;
	while (*str != '\0')
		str++;
	while (len-- > 0 && *s2 != '\0')
		*str++ = *s2++;
	*str = '\0';
	return (s1);
}
